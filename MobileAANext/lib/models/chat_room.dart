enum MessageType { text, news_share }

class ChatMessage {
  final String id;
  final String senderId;
  final String senderName;
  final DateTime timestamp;
  final MessageType type;
  
  // Text message
  final String? text;
  
  // News share
  final String? newsId;
  final String? newsTitle;
  final String? newsImageUrl;
  final String? emoji;
  final bool includeDetail;
  final String? autoGeneratedText; // AI Ã¼retimi

  ChatMessage({
    required this.id,
    required this.senderId,
    required this.senderName,
    required this.timestamp,
    required this.type,
    this.text,
    this.newsId,
    this.newsTitle,
    this.newsImageUrl,
    this.emoji,
    this.includeDetail = false,
    this.autoGeneratedText,
  });

  Map<String, dynamic> toJson() => {
        'id': id,
        'senderId': senderId,
        'senderName': senderName,
        'timestamp': timestamp.toIso8601String(),
        'type': type.toString(),
        'text': text,
        'newsId': newsId,
        'newsTitle': newsTitle,
        'newsImageUrl': newsImageUrl,
        'emoji': emoji,
        'includeDetail': includeDetail,
        'autoGeneratedText': autoGeneratedText,
      };

  factory ChatMessage.fromJson(Map<String, dynamic> json) => ChatMessage(
        id: json['id'],
        senderId: json['senderId'],
        senderName: json['senderName'],
        timestamp: DateTime.parse(json['timestamp']),
        type: MessageType.values.firstWhere(
          (e) => e.toString() == json['type'],
        ),
        text: json['text'],
        newsId: json['newsId'],
        newsTitle: json['newsTitle'],
        newsImageUrl: json['newsImageUrl'],
        emoji: json['emoji'],
        includeDetail: json['includeDetail'] ?? false,
        autoGeneratedText: json['autoGeneratedText'],
      );

